def get_all_stops() -> set[str]:
    """Отримати всі унікальні зупинки з malyn_transport.json."""
    data = json.loads(TRANSPORT_PATH.read_text(encoding="utf-8"))
    sbr = data.get("supplement", {}).get("stops", {}).get("stops_by_route", {})
    stops = set()
    for arr in sbr.values():
        stops.update(arr)
    stops.update(["Малинівка", "Юрівка", "БАМ", "Царське село"])
    return stops


def geocode(addr: str) -> Optional[list[float]]:
    """Геокодувати адресу через Nominatim. Повертає [lat, lon] або None."""
    url = (
        "https://nominatim.openstreetmap.org/search?"
        + urllib.parse.urlencode({"q": addr + ", Україна", "format": "json", "limit": "1"})
    )
    req = urllib.request.Request(url, headers={"User-Agent": "MalynTransport/1.0 (https://github.com/drivling/kyiv-malyn-booking)"})
    try:
        with urllib.request.urlopen(req, timeout=15) as r:
            data = json.loads(r.read().decode())
            if data:
                return [float(data[0]["lat"]), float(data[0]["lon"])]
    except Exception as e:
        print(f"  Помилка: {e}")
    return None


def main() -> None:
    print("Геокодування зупинок Малина (Nominatim, 1 req/сек)")
    print("=" * 50)

    all_stops = get_all_stops()
    print(f"Усього зупинок: {len(all_stops)}")

    existing: dict[str, list[float]] = {}
    if COORDS_PATH.exists():
        data = json.loads(COORDS_PATH.read_text(encoding="utf-8"))
        existing = data.get("stops", {})
        print(f"Існуючих координат: {len(existing)}")

    coords: dict[str, list[float]] = dict(existing)
    for s in all_stops:
        if s not in coords:
            coords[s] = MALYN_CENTER

    to_geocode = [s for s in all_stops if s in STOP_ADDRESSES]

    if not to_geocode:
        print("Немає зупинок для геокодування (немає адрес у словнику).")
        return

    print(f"Буде геокодовано: {len(to_geocode)} (з адресами з zupinki)")
    print()

    for i, stop in enumerate(sorted(to_geocode), 1):
        addr = STOP_ADDRESSES.get(stop) or stop + ", Малин"
        print(f"[{i}/{len(to_geocode)}] {stop}")
        print(f"  Адреса: {addr}")

        result = geocode(addr)
        if result:
            coords[stop] = result
            print(f"  OK: {result[0]:.6f}, {result[1]:.6f}")
        else:
            coords[stop] = MALYN_CENTER
            print(f"  Fallback: центр Малина")

        time.sleep(1.1)

    out = {"center": MALYN_CENTER, "stops": coords}
    COORDS_PATH.write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")
    print()
    print(f"Збережено: {COORDS_PATH}")


if __name__ == "__main__":
    main()
